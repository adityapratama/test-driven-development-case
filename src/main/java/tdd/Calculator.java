package tdd;

public class Calculator {

	public int add(int i, int j) {
		return i + j;
	}

	public int minus(int i, int j) {
		return i - j;
	}

	public int multi(int i, int j) {
		return i * j;
	}

	public double devide(int i, int j) {
		return i / j;
	}

	public Object mod(int i, int j) {
		return i % j;
	}

}
